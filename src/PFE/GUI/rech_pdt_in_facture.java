    public static void AffichIdPdtIdFactFr (String IdFactFr,String IdPdt) {
      
      
      
      if(factfrpdt.RechIdPdtIdFactFr(IdFactFr, IdPdt)!=null){
          
          
   FactFrPdt  factFrPdt =  factfrpdt.RechIdPdtIdFactFr(IdFactFr, IdPdt);
   
   
   row[0]=factFrPdt.getIdFactFr();
   row[1]=factFrPdt.getIdPdt();
   row[2]=factFrPdt.getQteAchatPdt();
   row[3]=factFrPdt.getPrxAchatPdt();
 
   mode.addRow(row);
          
          
  if(mode.getRowCount()>=2){
  
       mode.removeRow(0);
   }          
          
          
          
            
     }else{
          
    mode.setRowCount(0);
    
    
     JLabel message = new JLabel("Objet Inexistant");
     JLabel title = new JLabel("Message Information !");
     UIManager.put("OptionPane.okButtonText", " Oui");
     UIManager.put("OptionPane.buttonFont", new Font("Times New Roman ",0,12)); 
     JOptionPane.showMessageDialog(null, message,title.getText(),JOptionPane.INFORMATION_MESSAGE);
          
      }
      
 

            
            
            
        }  

